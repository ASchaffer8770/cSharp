using System;

public class Calculator
{
    public Calculator
	{
		public int Add(int x, int y)
    {
        return x + y;
    }
    public int Subtract(int x, int y)
    {
        return x - y;
    }
    public Multiply(int x, int y)
    {
        return x * y;
    }
    public Divide(int x, int y)
    {
        return x / y;
    }
}
}
